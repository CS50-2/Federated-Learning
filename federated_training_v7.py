def local_train(model, train_loader, epochs=5, lr=0.01):
    criterion = nn.CrossEntropyLoss()
    optimizer = optim.SGD(model.parameters(), lr=lr)
    model.train()
    for epoch in range(epochs):
        for batch_x, batch_y in train_loader:
            optimizer.zero_grad()
            outputs = model(batch_x)
            loss = criterion(outputs, batch_y)
            loss.backward()
            optimizer.step()
    return model.state_dict()

# å®æ—¶è®°å½•è®­ç»ƒè¿‡ç¨‹ä¸­çš„lossç”¨åšFedgra
def local_train_fedgra_loss(model, train_loader, epochs=5, lr=0.01):
    criterion = nn.CrossEntropyLoss()
    optimizer = optim.SGD(model.parameters(), lr=lr)
    model.train()
    loss_sq_sum = 0.0

    for epoch in range(epochs):
        for batch_x, batch_y in train_loader:
            optimizer.zero_grad()
            outputs = model(batch_x)
            loss = criterion(outputs, batch_y)
            loss.backward()
            optimizer.step()
            loss_sq_sum += loss.item() ** 2   

    h_i = np.sqrt(loss_sq_sum) #æ”¾å¤§è®­ç»ƒè¿‡ç¨‹ä¸­ loss çš„ç´¯ç§¯ç¨‹åº¦ï¼Œä»è€Œå¢å¼ºå®¢æˆ·ç«¯ä¹‹é—´çš„åŒºåˆ†åº¦ã€‚
    return model, h_i  

