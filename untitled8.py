def prepend_zero(time_list, acc_list):
    acc_list = list(acc_list)  # 把 pandas.Series 转成 list
    min_len = min(len(time_list), len(acc_list))
    x = [0] + list(np.cumsum(time_list[:min_len]))
    y = [0] + acc_list[:min_len]
    return x, y

